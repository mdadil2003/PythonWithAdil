# Iterating Over List, Tuple, String and Dictionary Using for Loops in Python

li = ["Code", "with", "Aadil"]
for i in li:
    print(i)
    
tup = ("Code", "with", "Aadil")
for i in tup:
    print(i)

s = "Code"
for i in s:
    print(i)
    
d = dict({'x':123, 'y':354})
for i in d:
    print("%s  %d" % (i, d[i]))
    
set1 = {1, 2, 3, 4, 5, 6}
for i in set1:
    print(i)
    