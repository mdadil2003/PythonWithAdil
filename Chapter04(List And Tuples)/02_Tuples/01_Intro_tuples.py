# Tuples are similar to lists but they are immutable

a = (1, 2, 5, 6, False, "Hello")
print(a)
print(type(a))

tuple1 = (1,2,2,3,5,4,6) # Tuple with integers
tuple2 = ("Red", "Green", "Blue") # Tuple with strings
print(tuple1)
print(tuple2)

details = ("Abhijeet", 18, "FYBScIT", 9.8) # Tuple with mixed data 
print(details)