# Assignment operators:

a = 7-2  # Assign value of 7-2 to a
print(a)
b = 3
c = 7

b += 8    # increment the value of b by 8 and then assign it to b 
print(b)

a -= 2  # decrement the value of a by 2 and then assign it to a
print(a)

c /= 5  # divide the value of c by 5 and then assign it to c
print(c)
